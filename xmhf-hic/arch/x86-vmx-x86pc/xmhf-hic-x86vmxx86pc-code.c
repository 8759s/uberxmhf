//initialize GDT
static void _xcprimeon_cpu_x86_initializeGDT(void){

	asm volatile(
		"lgdt  %0 \r\n"
		"pushq	%1 \r\n"				// far jump to runtime entry point
		"pushq	$reloadsegs \r\n"
		"lret \r\n"
		"reloadsegs: \r\n"
		"movw	%2, %%ax \r\n"
		"movw	%%ax, %%ds \r\n"
		"movw	%%ax, %%es \r\n"
		"movw	%%ax, %%fs \r\n"
		"movw	%%ax, %%gs \r\n"
		"movw  %%ax, %%ss \r\n"
		: //no outputs
		: "m" (_gdt), "i" (__CS_CPL0), "i" (__DS_CPL0)
		: //no clobber
	);


}

